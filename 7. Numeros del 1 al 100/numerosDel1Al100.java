/* 7. Números del 1 al 100: Escribe un programa
      que imprima los números del 1 al 100. */
/*-------------------------------------------------------*/

public class numerosDel1Al100 {
    public static void main(String[] args) {

        // Imprimir los números del 1 al 100
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
}